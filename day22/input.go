func parse(raw string) []position {
	var ps []position

	ss := strings.Split(raw, "\n")
	mid := len(ss) / 2

	for j, s := range ss {
		for i, r := range s {
			if r == '#' {
				ps = append(ps, position{i - mid, j - mid})
			}
		}
	}

	return ps
}
